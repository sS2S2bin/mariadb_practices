package bookshop.vo;

public class AuthorVo {
	// 보통 칼럼과 동일, 조인해서 가져올 데이터 
	private Long no;
	private String name;
	public Long getNo() {
		return no;
	}
	public void setNo(Long no) {
		this.no = no;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
